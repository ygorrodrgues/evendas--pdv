    private void jButtonBuscarVendaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButtonBuscarVendaMouseClicked
        // TODO add your handling code here:
        venda = new Venda();        
        
        try{
            venda = sis.buscarVenda(Integer.parseInt(jTextFieldIdVenda.getText()));
            
            for(ItemVenda item : venda.getItens()){
                Object[] it = {item.getId(),item.getNome(), item.getPreco(), item.getQtde()};
                dtm.addRow(it);
            }
         }catch(Exception e){
             JOptionPane.showMessageDialog(null, "Valor inválido");
         }
        
          
    }//GEN-LAST:event_jButtonBuscarVendaMouseClicked

