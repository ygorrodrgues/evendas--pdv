    private void jButton1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton1MouseClicked
        // TODO add your handling code here:
        CupomDeTroca cupom = new CupomDeTroca();
        ArrayList<ItemVenda> itensTroca = new ArrayList<ItemVenda>();
        ItemVenda itemVenda;
        double valor = 0;
        
        int[] linhasSelecionadasTabela = jTableProdutos.getSelectedRows();
        System.out.println("Quantidade de linhas selecionadas " +  linhasSelecionadasTabela.length);
        
        for(int i = 0; i< linhasSelecionadasTabela.length; i++){
            itemVenda = new ItemVenda();
            itemVenda.setId(Integer.parseInt(jTableProdutos.getValueAt(i, 0).toString()));
            itemVenda.setNome(jTableProdutos.getValueAt(i, 1).toString());
            itemVenda.setPreco(Double.parseDouble(jTableProdutos.getValueAt(i, 2).toString()));
            itemVenda.setQtde(Integer.parseInt(jTableProdutos.getValueAt(i, 3).toString()));
            
            valor += Double.parseDouble(jTableProdutos.getValueAt(i, 2).toString());
            
            itensTroca.add(itemVenda);
        }
        
        cupom.setItens(itensTroca);
        cupom.setValor(valor);
        cupom.setVenda(venda);
        
        System.out.println(venda.getId());
        
        int numeroCupom = sis.registrarCupomDeTroca(cupom);
        
        cupom.setId(numeroCupom);
        
        imprimirCupomTroca(cupom);
    }//GEN-LAST:event_jButton1MouseClicked
    
    
    private void imprimirCupomTroca(CupomDeTroca cupom){
        CupomTrocaImpress cupomtrocaImpress = new CupomTrocaImpress(cupom);
        cupomtrocaImpress.setVisible(true);
    }
    
    
